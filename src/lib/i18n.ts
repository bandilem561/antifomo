export function getMessage(
  language: AppLanguage,
  key: string,
  fallback?: string,
): string {
  if (language === "zh-CN") {
    return MESSAGES["zh-CN"][key] || MESSAGES.en[key] || fallback || key;
  }
  if (language === "zh-TW") {
    return MESSAGES["zh-TW"][key] || MESSAGES["zh-CN"][key] || MESSAGES.en[key] || fallback || key;
  }
  return MESSAGES[language]?.[key] || MESSAGES.en[key] || MESSAGES["zh-CN"][key] || fallback || key;
}
